type MobileFoundationStringEntry = (typeof mobileFoundationStringEntries)[number];

export type MobileFoundationStringKey = MobileFoundationStringEntry[0];

function buildMobileFoundationStrings(locale: Locale): Record<MobileFoundationStringKey, string> {
  return Object.fromEntries(
    mobileFoundationStringEntries.map(([key, en, ar]) => [key, locale === "en" ? en : ar]),
  ) as Record<MobileFoundationStringKey, string>;
}

export const mobileFoundationStrings: Record<Locale, Record<MobileFoundationStringKey, string>> = {
  en: buildMobileFoundationStrings("en"),
  ar: buildMobileFoundationStrings("ar"),
};

export function getMobileFoundationString(
  locale: Locale,
  key: MobileFoundationStringKey,
): string {
  return mobileFoundationStrings[locale][key] ?? mobileFoundationStrings.en[key] ?? key;
}
